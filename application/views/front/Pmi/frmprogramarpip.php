function calculoFecha(fecha1,fecha2) {
  var fechaInicio = new Date(fecha1).getTime();
  var fechaFin    = new Date(fecha2).getTime();
  var tiempo = fechaFin-fechaInicio;
  var dias = Math.floor(tiempo / (1000 * 60 * 60 * 24));
  return dias;
}
